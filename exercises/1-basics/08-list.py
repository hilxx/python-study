# 리스트를 순회할 때는 의미있는 이름 사용
fruits = ["apple", "banana", "orange"]
print(fruits) # ['apple', 'banana', 'orange']
print(fruits[0])  # apple

# 추가
fruits.append("peach")

# 반복
for fruit in fruits:
    print(fruit)

for item in fruits:
    print(item)


